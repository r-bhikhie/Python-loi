# 6 Converteer tekst naar getal
# Converteer de tekst-variablen (t1,t2,t3)
# naar de getallen (g1,g2,g3)
# en bereken het product van die getallen
t1 = "117"
t2 = "19.3"
t3 = "-36.0"
# vul de toekenningen aan
g1 = int(t1)
g2 = float(t2)
g3 = float(t3)
product = g1*g2*g3
print(product)
