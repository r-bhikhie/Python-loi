getal1 = 25
getal2 = 3

sum = getal1 + getal2 
sub = getal1 - getal2 # vervang + door aftrekken
mul = getal1 * getal2 # vervang + door vermenigvuldigen
fdv = getal1 // getal2 # vervang + door deling zonder rest
idv = getal1 / getal2 # vervang + door deling met rest
mod = getal1 % getal2 # vervang + door modulo (geeft de rest)
exp = getal1 ** getal2 # vervang + door machtsverheffen

print(sum,sub, mul, fdv, idv, mod, exp) 

